DROP TABLE IF EXISTS NEWS;
CREATE TABLE NEWS (
  ID INT AUTO_INCREMENT,
  AUTHOR_ID INT,
  CAPTION VARCHAR(64),
  TEXT VARCHAR (2048),
  PRIMARY KEY(ID));

DROP TABLE IF EXISTS AUTHOR;
CREATE TABLE AUTHOR (
  ID INT AUTO_INCREMENT,
  NAME VARCHAR(128),
  PRIMARY KEY(ID));

